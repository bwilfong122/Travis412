package travis_ci_tutorial_java;

public class SimpleCalculator {
	//Add
	public int add(int a, int b) {
		return a + b;
	}
	
	//Subtract
	public int minus(int a, int b) {
		return a - b;
	}
	
	//Divide
	public int divide(int a, int b) {
		return a / b;
	}
	
	//Multiply
	public int multiply(int a, int b) {
		return a * b;
	}
}
